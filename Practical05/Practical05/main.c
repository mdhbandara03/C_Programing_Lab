#include <stdio.h>
#include <stdlib.h>

int main()
{
    //while loop
    int x=0;
    while(x<=100)
    {
      printf(" %d",x);
      x++;
    }
    //do while loop
    int x=0;
    do
    {
    printf("%d",x);
    x++;
    }while(x<=100);
    //for loop
    int x;
    for(x=0;x<=100;x++)
    {
     printf("%d",x);
    }


}
